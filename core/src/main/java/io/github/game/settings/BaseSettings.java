package io.github.game.settings;

/**
 * Базовый интерфейс для всех категорий настроек. Определяет общие методы для управления
 * настройками.
 */
public interface BaseSettings {

    /**
     * Загружает настройки из хранилища
     */
    void load();

    /**
     * Сохраняет настройки в хранилище
     */
    void save();

    /**
     * Сбрасывает настройки к значениям по умолчанию
     */
    void resetToDefaults();
}
